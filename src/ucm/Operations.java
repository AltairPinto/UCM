public class Operations {
    
    public int ADD(int reg_1, int reg_2){
        return reg_1 + reg_2;
    }
    
    public int SUB(int reg_1, int reg_2){
        return reg_1 - reg_2;
    }
    
    public int MUL(int reg_1, int reg_2){
        return reg_1 * reg_2;
    }
    
    public int DIV(int reg_1, int reg_2){
        return reg_1 / reg_2;
    }
    
}
